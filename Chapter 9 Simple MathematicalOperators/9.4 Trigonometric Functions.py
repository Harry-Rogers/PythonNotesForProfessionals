a,b = 1,2
import math
print(math.sin(a))
#prints sin(1) in rads
print(math.cosh(b))
#prints cosh(2) in rads
print(math.atan(math.pi))
#prints arc tan of pi in rads
print(math.hypot(a,b))
#prints euclidean norm same as math.sqrt(a*a + b*b)
print(math.degrees(a))
#prints degrees instead of rads
